ll n;

ll s(ll a, ll b) {
	return (a + b) * 2;
}

int32_t main() {
#define TASKNAME "perimeter"
	ios_base::sync_with_stdio (0);
	cin.tie (0);
	if ( fopen( TASKNAME".inp", "r" ) ) {
		freopen (TASKNAME".inp", "r", stdin);
		freopen (TASKNAME".out", "w", stdout);
	}
	cin >> n;
	ll ans = 1e18;
	for(ll i = 1; i <= n; i++) {
		if(n % i == 0) {
			ans = min(ans, s(i, n / i));
		}
	}
	cout << ans;
	return 0;
}
