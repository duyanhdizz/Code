int t;

int scs(ll n) {
	int res = 0;
	while(n > 0) {
		res++;
		n /= 10;
	}
	return res;
}

void solve() {
	ll n;
	cin >> n;
	if(n <= 9) cout << n << '\n';
	else {
		ll ans = (scs(n) - 2) * 9;
		ans += (n / pow(10, scs(n) - 1));
		cout << ans + 9 << '\n';
	}
}

int32_t main() {
#define TASKNAME "A"
	ios_base::sync_with_stdio (0);
	cin.tie (0);
	if ( fopen( TASKNAME".inp", "r" ) ) {
		freopen (TASKNAME".inp", "r", stdin);
		freopen (TASKNAME".out", "w", stdout);
	}
	cin >> t;
	while(t--)
		solve();
	return 0;
}
