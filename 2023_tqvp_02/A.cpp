vector<int> prime;
vector<int> lpf;
void sieve(int n) {
	prime.assign(1, 2);
	lpf.assign(n + 1, 2);

	for (int x = 3; x <= n; x += 2) {
		if (lpf[x] == 2) prime.push_back(lpf[x] = x);
		for (int i = 0; i < prime.size() && prime[i] <= lpf[x] && prime[i] * x <= n; ++i)
			lpf[prime[i] * x] = prime[i];
	}
}

long long binpowmod(long long a, long long b, long long m) {
	a %= m;
	long long res = 1;
	while (b > 0) {
		if (b & 1)
			res = res * a % m;
		a = a * a % m;
		b >>= 1;
	}
	return res;
}

long long binpow(long long a, long long b) {
	long long res = 1;
	while (b > 0) {
		if (b & 1)
			res = res * a;
		a = a * a;
		b >>= 1;
	}
	return res;
}

ll solve(ll k, ll m) {
	int _ = 1;
	while(binpow(k, _) <= m) _++;
	return _ - 1;
}

void SaKaTa() {
	int n;
	cin >> n;
	sieve(n);
	ll ans = 1;
	each(p, prime) {
		ans = ans * binpowmod(p, solve(p, n), mod) % mod;
	}
	cout << ans;
}

