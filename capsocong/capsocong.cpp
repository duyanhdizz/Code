int solve(vector<int>& v) {
	vector<int> a = v;
	sort(all(a));
	int d = min(a[1] - a[0], a[2] - a[1]);
	a.pb(INF);
	int i = 0;
	while(true) {
		if(a[i] + d != a[i + 1]) {
			return a[i] + d;
		}
		i++;
	}
}

