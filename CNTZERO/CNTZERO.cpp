long long binpow(long long a, long long b) {
    long long res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a;
        a = a * a;
        b >>= 1;
    }
    return res;
}

int trailing_zeros(int n) {
	if (n == 0) {
		return 0;
	}
	int k = 1;
	int tz = 0;
	while (binpow(5, k) <= n) {
		tz += n / binpow(5, k);
		k += 1;
	}
	return tz;
}

