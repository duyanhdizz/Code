const int N = 9 + 1e6;
const int INF = 1e9;

map<string, int> m;

bool check(char c) {
	return ('0' <= c && c <= '9');
}

int32_t main() {
#define TASKNAME "coci2021_r5_sifra"
	ios_base::sync_with_stdio (false);
	cin.tie (nullptr);
	cout.tie (nullptr);
	int tbegin = clock();
	if ( fopen( TASKNAME".inp", "r" ) ) {
		freopen (TASKNAME".inp", "r", stdin);
		freopen (TASKNAME".out", "w", stdout);
	}
	string s;
	cin >> s;
	int ans = 0;
	int i = 0;
	while(i < s.size()) {
		while(i < s.size() && !check(s[i])) i++;
		int j = i;
		while(j < s.size() && check(s[j])) j++;
		string res = s.substr(i, j - i);
		if(m[res] == 0 && i != j) ans++;
		m[res]++;
		i = j;
	}
	cout << ans;
	cerr << '\n' << "Runtime: " << (clock() - tbegin) << " ms\n";
	return 0;
}
