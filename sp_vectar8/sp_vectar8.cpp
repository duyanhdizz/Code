vector<int> prime;
vector<int> lpf;
void sieve(int n) {
	prime.assign(1, 2);
	lpf.assign(n + 1, 2);

	for (int x = 3; x <= n; x += 2) {
		if (lpf[x] == 2) prime.push_back(lpf[x] = x);
		for (int i = 0; i < prime.size() && prime[i] <= lpf[x] && prime[i] * x <= n; ++i)
			lpf[prime[i] * x] = prime[i];
	}
}

bool check(int x) {
	return (lpf[x] == x);
}

long long binpow(long long a, long long b) {
	long long res = 1;
	while (b > 0) {
		if (b & 1)
			res = res * a;
		a = a * a;
		b >>= 1;
	}
	return res;
}

bool solve(int x) {
	int _ = x % 10;
	if(_ == 0) return false;
	int __ = 0;
	if(check(_) == false) return false;
	x /= 10;
	while(x > 0) {
		__++;
		if(x % 10 == 0) return false;
		_ = (x % 10) * binpow(10, __) + _;
		x /= 10;
		if(check(_) == false) return false;
	}
	return true;
}

